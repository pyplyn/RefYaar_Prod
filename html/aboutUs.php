                <h2 style="text-align: center">The Million Dollar Idea</h2>
                <br/>
                
                <p style="text-align: justify;">
                    <strong>Employee Referrals.</strong>
What is the first thing that comes to your mind when you think of referring someone in your organization? You immediately think of your best friends, your family and your acquaintances. You start thinking of whom could you refer within that class pool, matching the required skill sets. Before long, you realise that, you actually have no idea about the professional or the technical expertise of your friends. You cannot make an informed decision whether your friends would be a good fit; I mean how can you? He is your friend; you are biased towards him. For an average employee, who has a very limited network to begin with, he is left with a very limited, most times nobody that he can refer to his company. This is the reason that when he comes across an opening in his organization, he ignores it, as he doesn’t know enough people to refer, even though he is aware had he referred somebody successfully, he would have earned a good bonus from the company. And this is precisely the reason, that employee referrals; even though they are the best and most transparent mode of hiring, only accounts for 25% of hiring, not just nationally but also internationally!
<br/>
<br/>
<strong>Sad isn’t it?</strong>
<br/>
<br/>
So this got us to thinking. What if we could leverage the reach of the common employee from the limited 10-15 people he knows, to the whole spectrum of job seeks out there? People who fit the skill sets required for the position and who are looking to come and work for the company he works in. If he had access to these job seekers, would he not refer them to his company? After all, there is a handsome bonus he stands to earn!
<br/>
<br/>
<strong>Then you will ask me; wait, isn’t that the job of the HR?</strong>
<br/>
<br/>
The reality is that, all that the HR does is selects some CVs and hands them over to you. You are the one who has to shortlist the candidates and if required, even take the interviews. Admit it, everyone of you at some point in your careers have gone through CVs and shortlisted people in your team. I have done it, my co-founder has done it, heck he does it every month, shortlisting from 10-20 CVs and passing them back to your HR. It is expected as part of your job description, and you don’t get paid a dime for doing this. The candidate you actually like and shortlist, even if he joins, will never know you vouched for him, you will never have any form of connect with him.
<br/>
<br/>
What we are proposing, is to hell with depending on the HR to dump CVs on you and then having to shortlist relevant candidates and not getting paid anything extra for this. We are giving you the power to post the position in your company on our portal, and we will run our algorithms and give you crisp set of candidates perfectly matching the requirements. Then go ahead and connect with them, chat with them, visit their social media profiles and build a connection with them. Take an informed decision and shortlist the best out of the lot to your HR. You were going to do this anyway. The beauty is, if the guys you recommended get the position, you not only are on boarding a person whom you know from before, someone you have built a connect with, but you also get to earn the referral bonus from your company!
<br/>
<br/>
<strong>And you can do this, not just once over your tenure with the company, but every month, multiple times! </strong>

